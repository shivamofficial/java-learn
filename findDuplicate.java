import java.util.ArrayList;
import java.util.*;
public class Solution {

	public static int findDuplicate(ArrayList<Integer> arr) {

	      int ans = 0;
    
    //XOR ing all array elements
    for(int i = 0; i<arr.size(); i++ ) {
    	ans = ans^arr.get(i);
    }
	
    //XOR [1, n-1]
    for(int i = 1; i<arr.size();i++ ) {
    	ans = ans^i;
    }
    return ans;
	}
}
